  /// Custom text styles for academic data using JetBrains Mono
  /// Monospace clarity for student IDs, grades, and numerical data
  static TextStyle dataTextStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor = isLight ? textPrimaryLight : textPrimaryDark;
    return GoogleFonts.jetBrainsMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0.5,
    );
  }

  /// Box shadow for subtle elevation (2-4dp blur radius, 20% opacity)
  static List<BoxShadow> cardShadow({required bool isLight}) {
    final Color shadowColor = isLight ? shadowLight : shadowDark;
    return [
      BoxShadow(
        color: shadowColor,
        blurRadius: 4.0,
        offset: const Offset(0, 2),
        spreadRadius: 0,
      ),
    ];
  }

  /// Box shadow for elevated elements
  static List<BoxShadow> elevatedShadow({required bool isLight}) {
    final Color shadowColor = isLight ? shadowLight : shadowDark;
    return [
      BoxShadow(
        color: shadowColor,
        blurRadius: 8.0,
        offset: const Offset(0, 4),
        spreadRadius: 0,
      ),
    ];
  }
